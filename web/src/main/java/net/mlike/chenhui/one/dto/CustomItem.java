package net.mlike.chenhui.one.dto;

public class CustomItem
{
    private String Name;
    private String Value;

    public String getName()
    {
        return this.Name;
    }

    public void setName(String name)
    {
        this.Name = name;
    }

    public String getValue()
    {
        return this.Value;
    }

    public void setValue(String value)
    {
        this.Value = value;
    }
}
